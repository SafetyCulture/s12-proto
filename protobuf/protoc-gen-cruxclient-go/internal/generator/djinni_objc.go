// GenerateDjinniObjC generates the .djinni.objc.h file for a proto file descriptor.
// The output is functionally equivalent to the C++ plugin's api_generator.cc
// GenerateDjinniObjC function (lines 494-557). It produces one Translator struct
// per message (including nested), skipping map-entry synthetics.
func GenerateDjinniObjC(rw protoplugin.ResponseWriter, fileDesc protoreflect.FileDescriptor) {
	w := codegen.NewWriter()

	path := string(fileDesc.Path())
	baseName := common.StripProto(path)

	// Path segments for name derivation
	paths := common.Tokenize(common.StripProto(path), "/")
	dirParts := paths[:len(paths)-1]
	dir := strings.Join(dirParts, "_")

	fileNameCamel := common.ToCamelCase(paths[len(paths)-1])

	// Proto file options
	opts := fileDesc.Options().(*descriptorpb.FileOptions)
	objcClassPrefix := opts.GetObjcClassPrefix()

	// Comment header + pragma once
	w.WL("// This file is generated by s12-proto api generator. Please DO NOT modify.")
	w.WL("#pragma once")

	// Per-message Translator blocks
	messages := common.GetMessagesFromFile(fileDesc)
	for _, msg := range messages {
		if msg.IsMapEntry() {
			continue
		}

		messageName := common.ClassName(msg, false)
		cppTypeName := common.DotsToColons(string(msg.FullName()))

		writeDjinniObjCBlock(w, baseName, dir, fileNameCamel,
			messageName, cppTypeName, objcClassPrefix)
	}

	rw.AddFile(baseName+".djinni.objc.h", w.String())
}

// writeDjinniObjCBlock writes a single ObjC Translator block for a message.
func writeDjinniObjCBlock(w *codegen.CodeWriter,
	baseName, dir, fileNameCamel,
	messageName, cppTypeName, objcClassPrefix string) {

	// #include and #import emitted per message (not once at top)
	w.WL("#include \"%s.pb.h\"", baseName)
	w.WL("#import \"%s_%s.pbobjc.h\"", dir, fileNameCamel)
	w.WL()

	w.WL("namespace djinni::%s {", cppTypeName)
	w.WL("struct Translator {")
	w.WL("  using CppType = ::%s;", cppTypeName)
	w.WL("  using ObjcType = %s%s*;", objcClassPrefix, messageName)
	w.WL("  using Boxed = Translator;")
	w.WL()

	// toCpp method
	w.WL("  static CppType toCpp(ObjcType message) {")
	w.WL("    assert(message);")
	w.WL("    NSData * data = [message data];")
	w.WL("    const void *bytes = [data bytes];")
	w.WL("    int byte_len = (int)[data length];")
	w.WL("    CppType cpp_message;")
	w.WL("    cpp_message.ParseFromArray(bytes, byte_len);")
	w.WL("    return cpp_message;")
	w.WL("  }")
	w.WL()

	// fromCpp method
	w.WL("  static ObjcType fromCpp(const CppType& message) {")
	w.WL("    size_t byte_size = message.ByteSizeLong();")
	w.WL("    void *bytes = malloc(byte_size);")
	w.WL("    message.SerializeToArray(bytes, static_cast<int>(byte_size));")
	w.WL("    NSData *data = [NSData dataWithBytes: bytes length: (int)byte_size];")
	w.WL("    free(bytes);")
	w.WL("    NSError *error;")
	w.WL("    return [%s%s parseFromData:data error:&error];", objcClassPrefix, messageName)
	w.WL("  }")

	w.WL("};")
	w.WL("}  //namespace djinni::%s", cppTypeName)
	w.WL()
}
