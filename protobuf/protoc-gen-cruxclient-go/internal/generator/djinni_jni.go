// GenerateDjinniJNI generates the .djinni.jni.h file for a proto file descriptor.
// The output is functionally equivalent to the C++ plugin's api_generator.cc
// GenerateDjinniJNI function (lines 559-655). It produces one JNIInfo struct and
// one Translator struct per message (including nested), skipping map-entry synthetics.
func GenerateDjinniJNI(rw protoplugin.ResponseWriter, fileDesc protoreflect.FileDescriptor) {
	w := codegen.NewWriter()

	path := string(fileDesc.Path())
	baseName := common.StripProto(path)

	// Proto file options
	opts := fileDesc.Options().(*descriptorpb.FileOptions)
	javaPackage := opts.GetJavaPackage()
	javaPackageSlashes := common.DotsToSlashs(javaPackage)

	// Comment header + pragma once + djinni_support.hpp (once at top)
	w.WL("// This file is generated by s12-proto api generator. Please DO NOT modify.")
	w.WL("#pragma once")
	w.WL("#include \"djinni_support.hpp\"")

	// Per-message JNIInfo + Translator blocks
	messages := common.GetMessagesFromFile(fileDesc)
	for _, msg := range messages {
		if msg.IsMapEntry() {
			continue
		}

		messageName := common.ClassName(msg, false)
		cppTypeName := common.DotsToColons(string(msg.FullName()))
		jniMessageName := common.UnderscoresToDollar(messageName)

		writeDjinniJNIBlock(w, baseName, cppTypeName,
			javaPackageSlashes, jniMessageName)
	}

	rw.AddFile(baseName+".djinni.jni.h", w.String())
}

// writeDjinniJNIBlock writes a single JNI JNIInfo+Translator block for a message.
func writeDjinniJNIBlock(w *codegen.CodeWriter,
	baseName, cppTypeName,
	javaPackageSlashes, jniMessageName string) {

	// #include emitted per message (no #import for JNI)
	w.WL("#include \"%s.pb.h\"", baseName)

	// No blank line between #include and namespace (differs from ObjC)
	w.WL("namespace djinni::%s {", cppTypeName)

	// JNIInfo struct
	w.WL("struct JNIInfo {")
	w.WL("  const GlobalRef<jclass> clazz { jniFindClass(\"%s/%s\")  };",
		javaPackageSlashes, jniMessageName)
	w.WL("  const jmethodID method_toBytes { jniGetMethodID(clazz.get(), \"toByteArray\", \"()[B\") };")
	w.WL("  const jmethodID method_byteSize { jniGetMethodID(clazz.get(), \"getSerializedSize\", \"()I\") };")
	w.WL("  const jmethodID method_fromBytes { jniGetStaticMethodID(clazz.get(), \"parseFrom\", \"([B)L%s/%s;\") };",
		javaPackageSlashes, jniMessageName)
	w.WL("};")
	w.WL()

	// Translator struct
	w.WL("struct Translator {")
	w.WL("  using CppType = ::%s;", cppTypeName)
	w.WL("  using JniType = jobject;")
	w.WL("  using Boxed = Translator;")
	w.WL()

	// toCpp method
	w.WL("  static CppType toCpp(JNIEnv* jniEnv, JniType j) {")
	w.WL("    assert(j != nullptr);")
	w.WL("    const auto& data = JniClass<JNIInfo>::get();")
	w.WL("    assert(jniEnv->IsInstanceOf(j, data.clazz.get()));")
	w.WL("    jbyteArray byteArray = static_cast<jbyteArray>(jniEnv->CallObjectMethod(j, data.method_toBytes));")
	w.WL("    auto byte_len = jniEnv->CallIntMethod(j, data.method_byteSize);")
	w.WL("    jbyte* byte = jniEnv->GetByteArrayElements(byteArray, NULL);")
	w.WL("    CppType cpp_message;")
	w.WL("    cpp_message.ParseFromArray(byte, byte_len);")
	w.WL("    jniEnv->ReleaseByteArrayElements(byteArray, byte, JNI_ABORT);")
	w.WL("    jniExceptionCheck(jniEnv);")
	w.WL("    return cpp_message;")
	w.WL("  }")
	w.WL()

	// fromCpp method
	w.WL("  static LocalRef<JniType> fromCpp(JNIEnv* jniEnv, const CppType& message) {")
	w.WL("    size_t size = message.ByteSizeLong();")
	w.WL("    jbyte* temp = new jbyte[size];")
	w.WL("    message.SerializeToArray(temp, static_cast<int>(size));")
	w.WL("    jbyteArray bytes = jniEnv->NewByteArray(size);")
	w.WL("    jniEnv->SetByteArrayRegion(bytes, 0, size, temp);")
	w.WL("    delete[] temp;")
	w.WL("    const auto& data = JniClass<JNIInfo>::get();")
	w.WL("    auto j = LocalRef<JniType>{jniEnv->CallStaticObjectMethod(data.clazz.get(), data.method_fromBytes, bytes)};")
	w.WL("    jniExceptionCheck(jniEnv);")
	w.WL("    return j;")
	w.WL("  }")

	w.WL("};")
	w.WL("}  //namespace djinni::%s", cppTypeName)
	w.WL()
}
